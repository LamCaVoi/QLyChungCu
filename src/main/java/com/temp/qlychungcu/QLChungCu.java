        public void openBangGia(){
        resetBangGia();
        int width =320;
        int height = 275;
        new Thread(new Runnable(){
            @Override
            public void run(){
                for (int i = 0; i < width; i++){
                    BangGia.setSize(i, height);
                    try {
                        Thread.sleep(2);
                    } catch (InterruptedException ex) {
                        Logger.getLogger(QLChungCu.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
            }
        }).start();
    }
    
    public void closeBangGia() {
        int width = 320;
        int height = 275;
        new Thread(new Runnable() {
            @Override
            public void run() {
                for (int i = width; i > 0; i--) {
                    BangGia.setSize(i, height);
                    try {
                        Thread.sleep(2);
                    } catch (InterruptedException ex) {
                        Logger.getLogger(QLChungCu.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
            }
        }).start();
    }
    

