    public boolean isEmptyNguoiThue() {
        if (TenNguoiThue.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập tên");
            return false;
        }

        if (NgayThangNamSinh.getDate() == null) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập ngày sinh");
            return false;
        }

        if (NgayThangNamSinh.getDate().compareTo(new Date()) > 0) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập ngày hợp lệ");
            return false;
        }

        if (SDT.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập số điện thoại");
            return false;
        }

        if (SDT.getText().length() >= 12) {
            JOptionPane.showMessageDialog(this, "Số điện thoại không phù hợp");
            return false;
        }

        if (SoCCCD.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập số CCCD/CMND");
            return false;
        }

        if (QueQuan.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập quê quán");
            return false;
        }

        if (IDPhong.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập ID phòng");
            return false;
        }
        return true;
    }

